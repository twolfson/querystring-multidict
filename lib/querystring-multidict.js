// Query String Utilities
// Based on https://github.com/nodejs/node/blob/v0.10.45/lib/querystring.js
//   and https://github.com/pallets/werkzeug/blob/0.11.10/werkzeug/urls.py#L774-L790
var QueryString = require('querystring');

// If obj.hasOwnProperty has been overridden, then calling
// obj.hasOwnProperty(prop) will break.
// See: https://github.com/joyent/node/issues/1707
function hasOwnProperty(obj, prop) {
  return Object.prototype.hasOwnProperty.call(obj, prop);
}

// Parse a key=val string.
exports.parse = exports.decode = function (qs, sep, eq, options) {
  sep = sep || '&';
  eq = eq || '=';
  var multidict = {};

  if (typeof qs !== 'string' || qs.length === 0) {
    return multidict;
  }

  var regexp = /\+/g;
  qs = qs.split(sep);

  var maxKeys = 1000;
  if (options && typeof options.maxKeys === 'number') {
    maxKeys = options.maxKeys;
  }

  var len = qs.length;
  // maxKeys <= 0 means that we should not limit keys count
  if (maxKeys > 0 && len > maxKeys) {
    len = maxKeys;
  }

  for (var i = 0; i < len; ++i) {
    var x = qs[i].replace(regexp, '%20');
    var idx = x.indexOf(eq);
    var kstr, vstr, k, v;

    if (idx >= 0) {
      kstr = x.substr(0, idx);
      vstr = x.substr(idx + 1);
    } else {
      kstr = x;
      vstr = '';
    }

    k = QueryString.unescape(kstr, true);
    v = QueryString.unescape(vstr, true);
    multidict.add(k, v);
  }

  return multidict;
};
